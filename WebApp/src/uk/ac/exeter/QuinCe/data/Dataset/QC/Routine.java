package uk.ac.exeter.QuinCe.data.Dataset.QC;

import java.util.List;
import java.util.Map;

import org.apache.commons.lang3.Range;

import uk.ac.exeter.QuinCe.jobs.files.AutoQCJob;
import uk.ac.exeter.QuinCe.utils.StringUtils;

/**
 * This interface specifies the minimum set of methods required for a any type
 * of QC routine in QuinCe.
 *
 * <p>
 * Each {@code Routine} consists of a human-readable name, and messages that are
 * applied to QC flags when they are set to anything that isn't considered Good
 * (see {@link FlagScheme#isGood(Flag, boolean)}).
 * </p>
 *
 * <p>
 * The messages are not necessarily stored in a QC result. For example,
 * {@link AutoQCJob}s will simply store the {@link Flag} and which
 * {@code Routine} triggered that flag. Calls to {@link #getShortMessage()} or
 * {@link #getLongMessage(RoutineFlag)} will then be used to retrieve the QC
 * message.
 * </p>
 */
public abstract class Routine {

  /**
   * Indicates whether or not this is a stub version of the Routine.
   *
   * <p>
   * Stubs can only be used to gain information about the Routine - they cannot
   * be used for actual QC. Stubs are used to look up the messages generated by
   * Routines.
   * </p>
   */
  private boolean stub = false;

  /**
   * The range limits set for this Routine.
   *
   * <p>
   * A number of ranges can be set that will trigger different {@link Flag}s if
   * a value is outside a range. The Routine will determine the most significant
   * triggered {@link Flag}. It is up to the caller to ensure that the
   * {@link Map} is logically consistent.
   * </p>
   */
  protected final Map<Flag, Range<Double>> limits;

  protected final FlagScheme flagScheme;

  public Routine(FlagScheme flagScheme) {
    this.stub = true;
    this.limits = null;
    this.flagScheme = flagScheme;
  }

  public Routine(FlagScheme flagScheme, Map<Flag, Range<Double>> limits) {
    this.flagScheme = flagScheme;
    this.limits = limits;
  }

  /**
   * The name of the routine.
   *
   * @return The routine name.
   */
  public abstract String getName();

  /**
   * Returns short version of the QC message reported by this {@code Routine}.
   *
   * <p>
   * This is a simple message that describes the type of check the routine
   * performed, without any details of the specific value(s) that caused a
   * {@link RoutineFlag} to be set.
   * </p>
   *
   * @return The short QC message for the {@code Routine}.
   */
  public abstract String getShortMessage();

  /**
   * Returns the fully detailed version of the QC message reported by this
   * {@code
   * Routine}.
   *
   * <p>
   * This will include specific details of why a {@link RoutineFlag} was raised
   * by the {@code Routine}, such as the expected value(s) versus the value(s)
   * in the data.
   * </p>
   *
   * @param flag
   *          The QC flag raised by the {@code Routine}, containing detailed
   *          information of the cause.
   * @return The long form QC message for the {@code Routine}.
   */
  public abstract String getLongMessage(RoutineFlag flag);

  /**
   * Get the QC {@link Flag} from this Routine based on the configured value
   * limits.
   *
   * <p>
   * Checks all the limits for the Routine, and finds those that the
   * {@code value} falls outside, and returns the most significant of those
   * ranges. If the {@code value} is within all ranges, the method returns
   * {@code null}.
   * </p>
   *
   * @param value
   *          The value to be checked.
   * @param reportMaxOnly
   *          If {@code true}, reports only the upper limit of each
   *          {@link Range} as the {@code requiredValue} of the resulting
   *          {@link RoutineFlag}. If {@code false}, reports the lower and upper
   *          limits.
   * @param limits
   *          The limits for the range check.
   * @return The calculated {@link Flag}.
   * @throws StubRoutineException
   *           If this is a {@link #stub} version of the {@code Routine}.
   *
   * @see FlagScheme
   * @see #limits
   */
  protected RoutineFlag getRangeFlag(double value, boolean reportMaxOnly,
    Map<Flag, Range<Double>> limits) throws StubRoutineException {

    stubCheck();

    RoutineFlag result = null;

    List<Flag> triggeredRangeFlags = limits.entrySet().stream()
      .filter(l -> !l.getValue().contains(value)).map(l -> l.getKey()).toList();

    if (triggeredRangeFlags.size() > 0) {
      Flag finalFlag = Flag.mostSignificant(triggeredRangeFlags);
      Range<Double> range = limits.get(finalFlag);
      String requiredValue = reportMaxOnly ? String.valueOf(range.getMaximum())
        : StringUtils.formatRange(range);

      result = new RoutineFlag(flagScheme, this, finalFlag, requiredValue,
        String.valueOf(value));
    }

    return result;
  }

  protected RoutineFlag getRangeFlag(double value, boolean reportMaxOnly)
    throws StubRoutineException {
    return getRangeFlag(value, reportMaxOnly, limits);
  }

  protected RoutineFlag getRangeFlag(double value) throws StubRoutineException {
    return getRangeFlag(value, false);
  }

  protected void stubCheck() throws StubRoutineException {
    if (stub) {
      throw new StubRoutineException(this);
    }
  }
}
